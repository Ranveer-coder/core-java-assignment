package com.kaizen.operators;
import java.lang.Math.*;
public class MathFunction {
	
	public static void main(String[] args) {
		
		int a = 7;
		int x = 17;
		
		Math.sqrt(a);
		Math.pow(2, 3);
		Math.sin(a);
		Math.sinh(x);
		Math.cos(a);
		Math.cosh(x);
		Math.acos(a);
		Math.acos(x);
		Math.tan(a);
		Math.tanh(x);
		Math.atan(a);
		Math.atan(x);
		Math.atan2(a, x);
		
		
		System.out.println(Math.sqrt(a));
		System.out.println(Math.pow(2, 3));
		System.out.println(Math.sin(a));
		System.out.println(Math.sinh(x));
		System.out.println(Math.cos(a));
		System.out.println(Math.cosh(x));
		System.out.println(Math.acos(a));
		System.out.println(Math.acos(x));
		System.out.println(Math.tan(a));
		System.out.println(Math.tanh(x));
		System.out.println(Math.atan(a));
		System.out.println(Math.atan(x));
		System.out.println(Math.atan2(a, x));
		
		
	}

}
