package com.kaizen.operators;

public class ArthemiticOperators {
	
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 5;
		
		System.out.println("Addition : a + b = " +(a + b));
		System.out.println("Substraction : a - b = " +(a - b));
		System.out.println("Division : a / b = " +(a / b));
		System.out.println("Multiplication : a * b = " +(a * b));
		System.out.println("Modulus(remainder) : a % b = " +(a % b));
		System.out.println("Increment : ++a = " +(++a));
		System.out.println("Increment : a++ = " +(a++));
		System.out.println("Increment : ++b = " +(++b));
		System.out.println("Increment : b++ = " +(b++));
		System.out.println("Decrement : --a = " +(--a));
		System.out.println("Decrement : a-- = " +(a--));
		System.out.println("Decrement : --b = " +(--b));
		System.out.println("Decrement : b-- = " +(b--));
		System.out.println("Addition Assignment : a += b = " +(a += b));
		System.out.println("Substraction Assignment : a -= b = " +(a -= b));
		System.out.println("Division Assignment : a /= b = " +(a /= b));
		System.out.println("Multiplication Assignment : a =* b = " +(a *= b));
		System.out.println("Modulus Assignment : a %= b = " +(a %= b));
		
		
		
		
	}

}
