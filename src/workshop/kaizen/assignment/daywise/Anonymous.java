package workshop.kaizen.assignment.daywise;

public class Anonymous {
	
	static void printArray(int arr[]) {
	
		for(int i = 0 ; i < arr.length ; i++) {
			System.out.println(arr[i]);
		}
		
	}

	public static void main(String[] args) {
		
		
		int[] arr = {3,4,5,6,7};
		printArray(arr);
		
		
		
	}

}
